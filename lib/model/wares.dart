class Wares{
  final int id;
  final String name;
  final String rarity;
  final String category;
  final String type;
  final String image;
  final String detail;
  final int price;
  final int quantity;

  Wares({
    required this.id, 
    required this.name, 
    required this.rarity, 
    required this.category, 
    required this.type, 
    required this.image, 
    required this.detail, 
    required this.price, 
    required this.quantity
  });
}